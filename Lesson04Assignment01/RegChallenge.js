function squareNumber() {

  let input = prompt('Enter a number');
  let result = 0;

  result = Math.pow(input, 2);

  alert(result);

}


function multiplyNumber() {

  let input1 = prompt('Enter first number');
  let input2 = prompt('Enter second number');
  let result = 0;

  result = input1 * input2;

  alert(result);

}
